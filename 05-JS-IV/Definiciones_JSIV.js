/// Definiciones

/// Objetos: Los Objetos son variables que contienen listas duplas (propiedad + valor) no ordenados. Es decir, tienen dentro
/// de su definicion un valor para cada propiedad que le definamos. Es sumamente util ya que nodependemos (como en los array)
/// de recordar la posicion de un valor guardado en la lista, solo necesitamos saber el nombre de esa propiedad o key.

/// Propiedades: Las propiedades son los nombres de los valores que asignamos al objeto. Las utilizamos para recordar a  
/// a que hace referencia el valor de lo que estamos guardando.

/// Metodos: Los metodos son funciones asignadas a objetos. 


/// Bucles for..in: El ciclo for.. in es un bucle que se utiliza para iterar en un objeto recorriendo sus propiedades y
/// valores.

/// Puntos vs Corchetes: Las notaciones de corchetes y de puntos, son dos formas de escribir y definir los objetos. 
/// Los puntos son mas faciles y comodos para definir algunas propiedades. Mientras algunas funciones como invocar una fucion
/// con un valor de una propiedad, se utilizara la notacion de corchetes. En esta, la propiedad invocada sera, siempre, 
/// como un string, entre comillas.