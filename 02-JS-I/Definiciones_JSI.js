/// Definiciones jsI

/// Variables: Las variables son datos almacenados en la memoria de la pc. Para diferenciar los datos que almacenaremos, y 
/// poder utilizarlos, a estas variables les asignamos nombres referenciales. 

/// String: Los String son un tipo de dato que se conforman de cadenas de caracteres. Necesariamente van entre comillas, dobles o simples.

/// funciones: Las funciones son formulas generales que se crean para ser invocadas (llamadas) en cualquier momento que 
/// necesitemos, asi no tenemos que estar reescribiendo ese codigo cada vez que lo necesitamos. Los Argumentos, son datos 
/// que le suministramos a las funciones para que estas puedan ejecutarse.
/// Y la palabra clave return, es la que posibilita el retorno de los resultados de lasejecuciones de las funciones hacia
/// nuestro flujo de codigo.

/// Declaraciones IF : Las declaraciones IF son comandos a ejecutar que dependen de una condicion. Si la condicion se 
/// cumple, se ejecutan lasintruciones. Sino se procedera al siguiente instruccion.

