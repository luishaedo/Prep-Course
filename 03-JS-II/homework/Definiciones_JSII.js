/// - ||: el operador "o", tambien analiza las declaraciones, este con solo tener una declaracion verdadera nos devolvera
/// true, solo sera false cuando todas las declaraiones lo sean.
/// - !: el operador "not", cambia el valor de la declaracion por su contrario. Es decir, si la declaracion es true pasa
